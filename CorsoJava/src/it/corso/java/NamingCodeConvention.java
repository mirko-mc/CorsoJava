/*		NAMING E CODE CONVENTIONS
 * 		serie di indicazioni sintattiche che rendono i sorgenti facilmente leggibili anche da chi non li ha scritti
 * 		non sono obbligatorie ma altamente consigliate
 * 		
 * 		PACKAGES
 * 		I package dovrebbero essere scritti con lettere minuscole (lowercase).
 * 		Il package dovrebbe iniziare con le estensioni dei domini com, edu, gov, mil, net, org, o con le due lettere che idenditificano una nazione (secondo lo standard ISO Standard 3166, 1981)
 * 		Esempi:
 * 			com.nomeazienda.entities
 * 			it.corsi.java
 * 			it.miosoftware.fatturazione
 * 		Avere package strutturati, consente di gestire meglio le classi all’interno di un progetto di grandi dimensioni.
 * 		Esempio di organizzazione di package per una web application Java:
 * 			it.corsi.java.forms 		conterrà classi di tipo “Form”
 * 			it.corsi.java.bean 			conterrà classi di tipo “JavaBean”
 * 			it.corsi.java.actions 		conterrà classi di tipo “Action”
 * 
 * 		CLASSI
 * 		i nomi delle classi devono essere sostantivi ed iniziare con la lettera maiuscola
 * 		Se il nome della classe contiene più sostantivi, ciascun sostantivo deve avere la prima lettera maiuscola
 * 		Utilizzare intere parole ed evitare acronimi e abbreviazioni tranne il caso in cui la sigla è più utilizzata della forma lunga (ad es. HTML o URL)
 * 			ESEMPI
 * 			class Studente
 * 			class OrdineDiVendita
 * 			class URL
 * 
 * 		INTERFACCE
 * 		valgono le stesse regole delle classi. alcuni sviluppatori preferiscono identificare le interfacce iniziando il nome con la lettera "I"
 * 			ESEMPI
 * 			interface StudenteServiceLocal
 * 			interface IFatturaServiceLocal
 * 
 * 		VARIABILI
 * 		Il nome deve indicare cosa rappresenta la variabile
 * 		Utilizzare nomi brevi, chiari e significativi
 * 		Il nome deve iniziare con la lettera minuscola
 * 		In caso di nome composto da più parole, a partire dalla seconda, tutte le parole devono iniziare con la lettera maiuscola
 * 			ESEMPI
 * 			String nome;
 * 			Srting codiceFiscale;
 * 		Le variabili non devono iniziare con i simboli underscore “_” o dollaro “$”
 * 		Le singole lettere “i”, “j”, “k”...vanno utilizzate solo per le variabili temporanee (ad esempio in un ciclo for)
 * 		Le lettere utilizzate comunemente sono:
 * 			- i, j, k, m, n per i numeri interi
 * 			- c, d, e per i caratteri
 * 			ESEMPI
 * 			for(int i = 0; i < 10; i++) { … }
 * 			char c = ‘a’;
 * 
 * 		COSTANTI
 * 		I nomi delle variabili costanti (cioè variabili che non cambiano mai il loro valore) devono essere scritti in maiuscolo (uppercase)
 * 		Se il nome della variabile è composto da più parole, separare ogni parola con il simbolo underscore (“_")
 * 			ESEMPI
 * 			public static final String NAZIONE_DEFAULT = ‘it’;
 * 			public static final int LARGHEZZA_MINIMA = 22;
 * 
 * 		METODI
 * 		I metodi devono essere verbi o devono iniziare con un verbo
 * 		Il nome deve iniziare con la lettera minuscola
 * 		In caso di nome composto da più parole, a partire dalla seconda, tutte le parole devono iniziare con la lettera maiuscola
 * 			ESEMPI
 * 			public String getNome() {… }
 * 			public String getCodiceFiscale() { … }
 * 			private stampaFattura(Long idFattura) { … }
 * */
